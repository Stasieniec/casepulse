// Generates worker/lib/pdf-data.ts by embedding the 4 exhibit PDFs as
// base64 strings. Workers cannot read the filesystem at runtime, so the
// PDF bytes are baked into a committed module. Re-run with:
//   node scripts/gen-pdf-data.mjs
import { readFileSync, writeFileSync } from 'node:fs'
import { join, dirname } from 'node:path'
import { fileURLToPath } from 'node:url'

const root = join(dirname(fileURLToPath(import.meta.url)), '..')
const pdfsDir = join(root, 'seed', 'pdfs')
const outFile = join(root, 'worker', 'lib', 'pdf-data.ts')

/** Filename prefix → document id */
const FILE_TO_ID = {
  '07_Change_Order_No_3': 'D07',
  '08_UAT_Acceptance_Certificate': 'D08',
  '09_Email_Go-Live_Readiness': 'D09',
  '19_Expert_Report_Whitfield_IT': 'D19',
}

const entries = Object.entries(FILE_TO_ID).map(([filename, docId]) => {
  const filePath = join(pdfsDir, `${filename}.pdf`)
  const bytes = readFileSync(filePath)
  const b64 = bytes.toString('base64')
  return `  ${JSON.stringify(docId)}: ${JSON.stringify(b64)},`
})

const banner =
  '// AUTO-GENERATED by scripts/gen-pdf-data.mjs — do not edit by hand.\n' +
  '// Embeds the seed/pdfs/*.pdf exhibit files as base64 so the Worker can\n' +
  '// pass them to Google Document AI at runtime (Workers have no filesystem access).\n\n'

const body =
  'export const DOC_PDFS: Record<string, string> = {\n' +
  entries.join('\n') +
  '\n}\n'

writeFileSync(outFile, banner + body)
console.log(`Wrote ${entries.length} PDFs → ${outFile}`)
