// Generates worker/lib/docs-data.ts by embedding every seed/docs/*.txt as a
// string literal. Workers cannot read the filesystem at runtime, so the 18
// evidence document texts are baked into a committed module. Re-run with:
//   node scripts/gen-docs-data.mjs
import { readdirSync, readFileSync, writeFileSync } from 'node:fs'
import { join, dirname, basename } from 'node:path'
import { fileURLToPath } from 'node:url'

const root = join(dirname(fileURLToPath(import.meta.url)), '..')
const docsDir = join(root, 'seed', 'docs')
const outFile = join(root, 'worker', 'lib', 'docs-data.ts')

const files = readdirSync(docsDir)
  .filter((f) => f.endsWith('.txt'))
  .sort()

const entries = files.map((f) => {
  const id = basename(f, '.txt') // e.g. "D19"
  const text = readFileSync(join(docsDir, f), 'utf8')
  // JSON.stringify gives us a safely-escaped JS string literal.
  return `  ${JSON.stringify(id)}: ${JSON.stringify(text)},`
})

const banner =
  '// AUTO-GENERATED by scripts/gen-docs-data.mjs — do not edit by hand.\n' +
  '// Embeds the seed/docs/*.txt evidence documents so the Worker can serve\n' +
  '// their full text at runtime (Workers have no filesystem access).\n\n'

const body =
  'export const DOC_TEXTS: Record<string, string> = {\n' +
  entries.join('\n') +
  '\n}\n'

writeFileSync(outFile, banner + body)
console.log(`Wrote ${files.length} documents → ${outFile}`)
